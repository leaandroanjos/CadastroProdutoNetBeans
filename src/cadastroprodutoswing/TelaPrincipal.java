    private void tableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdutosMouseClicked
        int linhaSelecionada = tableProdutos.getSelectedRow();
        Integer sku = (Integer) tableProdutos.getValueAt(linhaSelecionada, 0);
        
        Produto produto = listadeprodutos.get(sku);
       txtSKU.setText(String.valueOf(produto.getSku()));
       txtNome.setText(produto.getNome());
       txtPreco.setText(String.valueOf(produto.getValor()));
    }//GEN-LAST:event_tableProdutosMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tableProdutos.getSelectedRow();
        if (linhaSelecionada >= 0){
            int result = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir esse produto?", "Atencão!",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if (result == JOptionPane.YES_OPTION){
                Integer sku = (Integer) tableProdutos.getValueAt(linhaSelecionada, 0);
                listadeprodutos.remove(sku);
                modelo.removeRow(linhaSelecionada);
                
                JOptionPane.showMessageDialog(null, "Cliente Excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }
                    
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum produto selecionado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

   
