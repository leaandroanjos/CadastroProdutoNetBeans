    private void validacaoCampos(String nome, String SKU, String preco) {
        if ((nome == null || "".equals(nome))||(SKU == null || "".equals(SKU))||(preco == null || "".equals(preco))){
            JOptionPane.showMessageDialog(null,"Existem campos não preenchido, preencha-os!", "Erro",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    private void initCustomComponents() {
        modelo.addColumn("Sku");
        modelo.addColumn("Nome");
        modelo.addColumn("Preço");
       
        
        tableProdutos.setModel(modelo);
    }

    private void validacaoProduto(String SKU, Produto produto) {
        if (listadeprodutos.containsKey(Integer.valueOf(SKU))){
            JOptionPane.showMessageDialog(null,"Já existe um produto com esse SKU cadastrado", "Erro",JOptionPane.ERROR_MESSAGE);
        } else{
            listadeprodutos.put(Integer.valueOf(SKU), produto);
            modelo.addRow(new Object[]{produto.getSku(), produto.getNome(), produto.getValor()});
            limparCampos();
        }
    }

    private void limparCampos() {
       txtSKU.setText("");
       txtNome.setText("");
       txtPreco.setText("");
    }
